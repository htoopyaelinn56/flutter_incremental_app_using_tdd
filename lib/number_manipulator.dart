class NumberManipulator {
  int increment(int number) {
    return number + 1;
  }

  int increaseTwo(int number) {
    return number + 2;
  }

  int decrement(int number) {
    return number - 1;
  }

  int decreaseTwo(int number) {
    return number - 2;
  }

  int multiplyByTwo(int number) {
    return number * 2;
  }

  int divideByTwo(int number) {
    return number ~/ 2;
  }
}
